// A wrapper to call a do_function with a constant argument
// -- Ksilyan 2006-mar-07
inline void dofun_wrapper( DO_FUN * fn, Character * ch, const char * argument )
{
	char * buf = new char [ strlen(argument) + 1 ];
	strcpy(buf, argument);
	(*fn) (ch, buf);
	delete [] buf;
}


